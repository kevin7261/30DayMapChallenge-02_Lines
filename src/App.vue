   *    - 利用 position: fixed 實現真正的全螢幕體驗
   *
   * 2. 模組化組件架構
   *    - 將功能拆分到獨立的組件中
   *    - 組件間通過 props 和 events 進行通信
   *    - 保持組件的可重用性和可測試性
   *
   * 3. 響應式優先設計
   *    - 使用 Bootstrap 5 的響應式工具類
   *    - 確保在各種設備上都有良好的用戶體驗
   *    - 採用移動優先（mobile-first）的設計策略
   *
   * ==============================================================================
   * 📦 組件結構說明 (Component Structure)
   * ==============================================================================
   *
   * 內容層：路由視圖容器，動態顯示不同頁面組件
   * - 使用 Vue Router 的 <router-view> 組件
   * - 根據當前路由路徑動態渲染對應的頁面組件
   * - 支援路由過渡動畫（可選，當前未啟用）
   *
