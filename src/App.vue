   * 📚 相關文件 (Related Files)
   * ==============================================================================
   *
   * - ./main.js - 應用程式入口文件，掛載本組件
   * - ./router/index.js - 路由配置，定義頁面導航規則
   * - ./views/HomeView.vue - 首頁組件，展示地圖功能
   * - ./assets/css/common.css - 全局樣式定義
   *
   * ==============================================================================
