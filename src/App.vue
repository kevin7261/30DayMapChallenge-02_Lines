          1. 當用戶訪問不同的 URL 時，Vue Router 根據路由配置匹配對應的組件
          2. <router-view> 會自動渲染該組件並替換之前的內容
          3. 實現了單頁應用程式（SPA）的核心功能

          當前路由配置（./router/index.js）：
          - 路徑 "/" 對應 HomeView 組件
          - 用戶訪問首頁時，這裡會渲染 HomeView 組件

          可選特性：
          - 路由過渡動畫：可以包裹 <transition> 組件實現頁面切換動畫
          - 路由守衛：在路由配置中定義 beforeEach、afterEach 等守衛
